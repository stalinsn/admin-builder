export type DataDictionaryColumn = {
  name: string;
  type: string;
  description: string;
  required?: boolean;
  primaryKey?: boolean;
  unique?: boolean;
  indexed?: boolean;
};

export type DataDictionaryTable = {
  id: string;
  domain: string;
  label: string;
  tableName: string;
  description: string;
  columns: DataDictionaryColumn[];
  notes?: string[];
};

export type InternalDataDictionary = {
  generatedAt: string;
  persistence: {
    panelUsers: string;
    panelSettings: string;
    analyticsEvents: string;
    dataStudio: string;
    accountWorkspace: string;
    integrationApi: string;
  };
  modeledEntities: Array<{
    id: string;
    label: string;
    slug: string;
    tableName: string;
    description: string;
    status: 'draft' | 'ready';
    fields: DataFieldDefinition[];
  }>;
  systemTables: DataDictionaryTable[];
};

function column(
  name: string,
  type: string,
  description: string,
  options?: Partial<Pick<DataDictionaryColumn, 'required' | 'primaryKey' | 'unique' | 'indexed'>>,
): DataDictionaryColumn {
  return {
    name,
    type,
    description,
    ...options,
  };
}

function getMode(name: string, fallback = 'hybrid'): string {
  const value = process.env[name]?.trim().toLowerCase();
  return value === 'files' || value === 'database' ? value : fallback;
}

